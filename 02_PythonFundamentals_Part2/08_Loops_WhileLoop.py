'''
While loops are used to execute a block of code repeatedly as long as a given condition is true. The syntax for a while loop in Python is as follows:
while condition:
    # code block to be executed
    # update the condition if necessary
    # The condition is evaluated before each iteration of the loop. If the condition is true, the code block inside the loop is executed. After executing the code block, the condition is checked again. This process continues until the condition becomes false.
   
'''
n = 5  # initializing the value of n to 5
while(n >0):
    print("Number: ",n)
    n -= 1  # decrementing the value of n by 1 in each iteration    


