'''
write a program to print numbers using for loop and
 range function in Python. 
 The range function generates a sequence of numbers
 starting from 0 by default, and increments by 1 (by default), and stops before a specified number. In this case, we will use the range function to generate numbers from 1 to 10 and print them using a for loop.
'''

for num in range(1,11):  # using range to generate numbers from 1 to 10
    print(num)  # printing the current value of num


