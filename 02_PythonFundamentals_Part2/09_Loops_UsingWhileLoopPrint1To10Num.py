'''
Print 1 to 10 using a while loop in Python. The while loop will continue to execute as long as the condition is true. In this case, we will start with a variable initialized to 1 and increment it until it reaches 10.
'''

num = 1  # initializing the value of num to 1

while(num <=10):
    print(num)  # printing the current value of num
    num += 1  # incrementing the value of num by 1 in each iteration

print("Finished printing numbers from 1 to 10.")